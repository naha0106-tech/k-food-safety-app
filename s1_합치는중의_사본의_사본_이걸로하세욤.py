def get_user_profile_interactive(top_situations):
    clear_screen()
    print("👩‍🍳 [Step 1/3] 현지 찐친 장금이의 맵부심 테스트\n")
    print("Q1. 한국의 매운맛, 어디까지 감당 가능해?")

    print("  2: 신라면 정도는 쌉가능 (Medium - 일반인)")
    print("  3: 불닭볶음면 가져와! (Extreme - 맵고수)")
    spicy_level = get_valid_input(">> 번호 선택 (1~3): ", ["1", "2", "3"])
    spicy_limit = 2 if spicy_level == '1' else (3 if spicy_level == '2' else 5)

    clear_screen()
    print("👩‍🍳 [Step 2/3] 오늘의 무드 파악\n")
    print("Q2. 지금 네 상황에 가장 딱 맞는 걸 골라봐!")

    valid_sit_options = []
    for i, sit in enumerate(top_situations):
        print(f"  {i+1}: {sit}")
        valid_sit_options.append(str(i+1))

    pass_num = str(len(top_situations)+1)
    print(f"  {pass_num}: 딱히 없음 (패스)")
    valid_sit_options.append(pass_num)

    sit_choice = get_valid_input(f">> 번호 선택 (1~{pass_num}): ", valid_sit_options)
    chosen_situation = top_situations[int(sit_choice)-1] if sit_choice != pass_num else ""

    clear_screen()
    print("👩‍🍳 [Step 3/3] 왓챠식 정밀 입맛 분석\n")
    print("Q3. 네 입맛을 정확히 알아야 기깔난 걸 주지. 아래 음식들을 평가해봐!")
    print("  [ 1: Bad(불호) / 2: Soso(보통) / 3: Good(호) / 4: Very Good(극호) ]\n")

    user_vectors = []
    for food_name, features in baseline_foods.items():
        rating = get_valid_input(f"  - {food_name} 어때? (1~4): ", ["1", "2", "3", "4"])
        weight = rating_weights[rating]
        food_vector = np.array(features["taste"] + features["texture"])
        user_vectors.append(food_vector * weight)

    user_vector = np.sum(user_vectors, axis=0)
    v_min, v_max = np.min(user_vector), np.max(user_vector)

    if v_max - v_min == 0:
        normalized_user_vector = np.zeros_like(user_vector, dtype=float)
    else:
        normalized_user_vector = (user_vector - v_min) / (v_max - v_min)

    clear_screen()
    print("✨ 네 입맛 분석 완료! 장금이가 최적의 꿀조합을 찾고 있어...\n")
    time.sleep(1.5)
    return spicy_limit, chosen_situation, normalized_user_vector


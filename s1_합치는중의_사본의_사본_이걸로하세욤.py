# 6. 매칭 알고리즘 및 큐레이션 결과 출력
# ==========================================
def recommend_kfood(food_db, spicy_limit, situation, user_vector):
    candidates = []

    for food in food_db:
        if food.get("spiciness", 1) > spicy_limit: continue

        taste, texture = food["taste"], food["texture"]
        food_vector = np.array([
            taste.get("sweet", 1), taste.get("salty", 1), taste.get("rich", 1), taste.get("sour", 1),
            texture.get("crispy", 1), texture.get("chewy", 1), texture.get("soft", 1), texture.get("thick", 1), texture.get("popping", 1)
        ])

        if np.linalg.norm(user_vector) > 0 and np.linalg.norm(food_vector) > 0:
            sim = cosine_similarity(user_vector.reshape(1, -1), food_vector.reshape(1, -1))[0][0]
        else:
            sim = 0

        db_situations = food.get("situations", [])
        if situation and situation in db_situations:
            sim += 0.5

        candidates.append({
            "name": food.get("combination_name", "비밀의 꿀조합"),
            "score": sim,
            "original": food.get("original_snippet", "말로 다 표현 못 할 극락의 맛!")
        })

    candidates = sorted(candidates, key=lambda x: x["score"], reverse=True)

    clear_screen()
    print("="*75)
    print("🎉 장금이가 편의점 다 털어서 찾아낸 네 맞춤형 꿀조합 TOP 3 🎉")
    print("="*75)

    if not candidates:
        print("\n앗... 네 조건에 딱 맞는 음식이 지금 편의점에 다 털렸나봐 ㅠㅠ 매운맛을 조금 올려볼까?\n")
        return

    if situation:
        custom_comment = situation_comments.get(situation, f"네가 딱 원하던 '{situation}' 맞춤형 조합이야! 무조건 먹어봐.")
        print(f"✨ [오늘의 무드: {situation}] {custom_comment}\n")
    else:
        print(f"✨ [입맛 저격] 상황을 떠나서 네 입맛 데이터가 멱살 잡고 끌고 온 미친 조합이야!\n")

    for i, item in enumerate(candidates[:3]):
        match_rate = math.ceil(min(max(item['score'] * 50 + 50, 0), 99))

        name = item['name']
        if '+' in name:
            ingredients = " ➕ ".join([ing.strip() for ing in name.split('+')])
        else:
            ingredients = name

        print(f"🥇 [{i+1}위] {name}")
        print(f"   🔥 취향 일치율: {match_rate}%")
        print(f"   🛒 장바구니 필수템: {ingredients}")
        print(f"   💡 장금이의 조리법 & 꿀팁:\n      \"{item['original']}\"")
        print("-" * 75)


def get_valid_input(prompt_text, valid_options):
    time.sleep(0.3)
    while True:
        choice = input(prompt_text).strip()
        if choice in valid_options:
            return choice
        print("  ❌ 앗! 목록에 있는 번호를 정확히 입력해줘.\n")
        time.sleep(0.3)


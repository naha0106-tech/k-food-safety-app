def get_ai_inferred_ingredients(dish_name):
    """
    공공데이터에 없는 요리에 대해 AI에게 일반적인 재료를 추정해달라고 요청합니다.
    """
    INFERENCE_PROMPT_TEMPLATE = f"""
    너는 요리에 대한 깊은 지식을 가진 AI 조수이다.
    다음 음식 '{dish_name}'에 일반적으로 포함되는 주요 재료들을 5개 이내로 콤마로 구분하여 나열해라.
    예시: "돼지고기, 마늘, 양파, 김치, 파"
    """
    try:
        response = gemini_model.generate_content(
            INFERENCE_PROMPT_TEMPLATE,
            generation_config={'temperature': 0.5}
        )
        raw_ingredients_str = response.text.strip()
        # 콤마로 분리된 재료 문자열을 리스트로 변환
        ingredients = [ing.strip() for ing in raw_ingredients_str.split(',') if ing.strip()]
        return ingredients
    except Exception as e:
        print(f"⚠️ AI 재료 추정 중 에러 발생: {e}. Fallback 데이터를 사용합니다.")
        # AI 추정 실패 시 Fallback 데이터 (단순화된 예시)
        fallback_ingredients = {
            "파채": ["파", "고춧가루", "참기름"],
            "삼겹살": ["돼지고기", "소금", "후추"]
        }
        return fallback_ingredients.get(dish_name, [])


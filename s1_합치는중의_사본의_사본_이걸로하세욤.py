def build_db_map_from_csv(csv_path, target_dishes):
    """
    제공된 전북특별자치도 공공데이터 CSV 파일을 열어
    지정된 음식명(요리명)에 해당하는 실데이터 '재료내용'을 파싱하여 DB_Map을 빌드합니다.
    """
    # CSV 읽기 시도
    try:
        df = pd.read_csv(csv_path, encoding='cp949')
    except:
        df = pd.read_csv(csv_path, encoding='utf-8')

    print(f"DEBUG: DataFrame columns: {df.columns.tolist()}") # Debug line to print columns

    db_map = {}

    for dish in target_dishes:
        # '음식명' 컬럼에서 해당 음식을 검색 (정확히 일치하는 요리명만)
        matched = df[df['음식명'] == dish]

        if not matched.empty:
            # 실데이터의 '재료' 텍스트 추출 (예: "재료: 된장 3큰술, 두부 1/2모, ...")
            raw_text = str(matched.iloc[0]['재료'])

            # 정규식을 이용해 계량 단위(g, 큰술, 모 등) 및 숫자, 특수문자를 지우고 순수 식재료 명사만 추출
            clean_text = re.sub(r'[^가-힣\s,]', '', raw_text)  # 한글, 공백, 쉼표만 남김
            tokens = [t.strip() for t in clean_text.replace("재료", "").replace("주재료", "").replace("부재료", "").replace(",", " ").split()]

            # 불필요한 단어 제거 및 유니크한 재료 명사 리스트화 (최소 2글자 이상)
            ingredients = list(set([tok for tok in tokens if len(tok) >= 2 and tok not in ["큰술", "작은술", "약간", "적당량"]]))
            db_map[dish] = ingredients
        else:
            print(f"⚠️ [공공데이터 경고] CSV 내에 '{dish}' 요리 데이터가 존재하지 않습니다. AI 추정 재료를 사용합니다.")
            # AI에게 재료 추정 요청
            inferred_ingredients = get_ai_inferred_ingredients(dish)
            db_map[dish] = inferred_ingredients

    return db_map

